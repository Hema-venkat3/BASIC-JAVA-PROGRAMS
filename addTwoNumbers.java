public class addTwoNumbers {
    public static void main(String args[]) {
       //DECLARING VARIABLES
        int num1, num2, num3;
        //initializing values for the variables.
        num1 = 250;
        num2 = 300;
        //adding two numbers
        num3 = num1 + num2;
        //displaying the result stored in the variable
        System.out.println("THE ADDITION OF TWO NUMBERS IS :" + num3);
    }
}
